package com.xynl.www.domain;

public class Post {
    private Integer id;

    private String title;

    private String time;

    private Integer age;

    private String address;

    private String userntroduction;

    private String ntroduction;

    private String post;

    private String uname;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getUserntroduction() {
        return userntroduction;
    }

    public void setUserntroduction(String userntroduction) {
        this.userntroduction = userntroduction;
    }

    public String getNtroduction() {
        return ntroduction;
    }

    public void setNtroduction(String ntroduction) {
        this.ntroduction = ntroduction;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }
}